module.exports = class Calculator {
    add(first_num, second_num){
        return first_num + second_num;
    }
    subtract(first_num, second_num){
        return first_num - second_num;
    }
    multiply(first_num, second_num){
        return first_num * second_num;
    }
    divide(first_num, second_num){
        return first_num / second_num;
    }
}